function Tabbed({ content }) {
  const [activeTab, setActiveTab] = useState(0);

  return (
    <div>
      <div className="tabs">
        {content.map((ele,index)=>
        <Tab num={index} activeTab={activeTab} onClick={setActiveTab} key={index} />
        )}
        <Tab num={4} activeTab={activeTab} onClick={setActiveTab} />
        
      </div>

      {activeTab <= content.length ? (
        <TabContent item={content.at(activeTab)} key={activeTab} onclick={setActiveTab} />
      ) : (
        <DifferentContent />
      )}
    </div>
  );
}

function Tab({ num, activeTab, onClick }) {
  return (
    <button
      className={activeTab === num ? "tab active" : "tab"}
      onClick={() => onClick(items=>num)}
    >
      Tab {num + 1}
    </button>
  );
}

